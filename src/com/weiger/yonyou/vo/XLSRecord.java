package com.weiger.yonyou.vo;

public class XLSRecord {
	
	int row;
	String appName;
	String busiAction;
	double value_2d;
	double value_3d;
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public String getBusiAction() {
		return busiAction;
	}
	public void setBusiAction(String busiAction) {
		this.busiAction = busiAction;
	}
	public double getValue_2d() {
		return value_2d;
	}
	public void setValue_2d(double value_2d) {
		this.value_2d = value_2d;
	}
	public double getValue_3d() {
		return value_3d;
	}
	public void setValue_3d(double value_3d) {
		this.value_3d = value_3d;
	}
	
	
	
}
